package model;

public class ModelProdutora {
	private int IDProd;
	private String Nome;
	private int Ativo;
	public int getIDProd() {
		return IDProd;
	}
	public void setIDProd(int iDProd) {
		IDProd = iDProd;
	}
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public int getAtivo() {
		return Ativo;
	}
	public void setAtivo(int ativo) {
		Ativo = ativo;
	}
}
