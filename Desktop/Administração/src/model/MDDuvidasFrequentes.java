package model;

public class MDDuvidasFrequentes {
	public int ID_DUVIDA;
	public String DUVIDA;
	public String RESPOSTA;
	public int ATIVO;
	
	public int getID_DUVIDA() {
		return ID_DUVIDA;
	}
	public void setID_DUVIDA(int iD_DUVIDA) {
		ID_DUVIDA = iD_DUVIDA;
	}
	public String getDUVIDA() {
		return DUVIDA;
	}
	public void setDUVIDA(String dUVIDA) {
		DUVIDA = dUVIDA;
	}
	public String getRESPOSTA() {
		return RESPOSTA;
	}
	public void setRESPOSTA(String rESPOSTA) {
		RESPOSTA = rESPOSTA;
	}
	public int getATIVO() {
		return ATIVO;
	}
	public void setATIVO(int aTIVO) {
		ATIVO = aTIVO;
	}
}
