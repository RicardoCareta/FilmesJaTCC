package Modelo;

public class ModelTipoEnvolvidos {
	private int	idTipoEnvo;
	private int tipo;
	private int ativo;

	public int getIdTipoEnvo() {
		return idTipoEnvo;
	}
	public void setIdTipoEnvo(int idTipoEnvo) {
		this.idTipoEnvo = idTipoEnvo;
	}
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	public int getAtivo() {
		return ativo;
	}
	public void setAtivo(int ativo) {
		this.ativo = ativo;
	}
}
