package Modelo;

public class ModelAvaliacaoCena {
	private int idAvCena;
	private int minuto;
	private int nota;
	private int idCat;
	private int idConta;
	private int ativo;
	
	public int getIdAvCena() {
		return idAvCena;
	}
	public void setIdAvCena(int idAvCena) {
		this.idAvCena = idAvCena;
	}
	public int getMinuto() {
		return minuto;
	}
	public void setMinuto(int minuto) {
		this.minuto = minuto;
	}
	public int getNota() {
		return nota;
	}
	public void setNota(int nota) {
		this.nota = nota;
	}
	public int getIdCat() {
		return idCat;
	}
	public void setIdCat(int idCat) {
		this.idCat = idCat;
	}
	public int getIdConta() {
		return idConta;
	}
	public void setIdConta(int idConta) {
		this.idConta = idConta;
	}
	public int getAtivo() {
		return ativo;
	}
	public void setAtivo(int ativo) {
		this.ativo = ativo;
	}
}
