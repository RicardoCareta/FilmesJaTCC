package Modelo;

public class ModelEnvolvidosTipo {
	private int idEnvo;
	private int idTipoEnvo;
	private int ativo;

	public int getIdEnvo() {
		return idEnvo;
	}
	public void setIdEnvo(int idEnvo) {
		this.idEnvo = idEnvo;
	}
	public int getIdTipoEnvo() {
		return idTipoEnvo;
	}
	public void setIdTipoEnvo(int idTipoEnvo) {
		this.idTipoEnvo = idTipoEnvo;
	}
	public int getAtivo() {
		return ativo;
	}
	public void setAtivo(int ativo) {
		this.ativo = ativo;
	}
}
